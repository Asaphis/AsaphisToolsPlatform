// Deduplicate by slug to avoid duplicate tool entries
const dedupedTools = Array.from(new Map(tools.map(t => [t.slug, t])).values());

// Expose all tools; UI will route to in-browser or backend based on availability
export const implementedTools: Tool[] = dedupedTools;
export const IMPLEMENTED_SLUGS = new Set<string>(implementedTools.map(t => t.slug));

// Helper functions
export const getFeaturedTools = (): Tool[] => {
  return tools.filter(tool => tool.featured);
};

export const getPopularTools = (): Tool[] => {
  return tools.filter(tool => tool.popular).slice(0, 8);
};

export const getToolsByCategory = (category: string): Tool[] => {
  return tools.filter(tool => tool.category === category);
};

export const getToolBySlug = (slug: string): Tool | undefined => {
  return tools.find(tool => tool.slug === slug);
};

export const getToolCategories = () => {
  const categories = Array.from(new Set(tools.map(tool => tool.category)));
  return categories.map(category => ({
    id: category,
    name: category.charAt(0).toUpperCase() + category.slice(1),
    count: tools.filter(tool => tool.category === category).length
  }));
};

export const searchTools = (query: string): Tool[] => {
  const lowerQuery = query.toLowerCase();
  return tools.filter(tool => 
    tool.name.toLowerCase().includes(lowerQuery) ||
    tool.description.toLowerCase().includes(lowerQuery) ||
    tool.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};
